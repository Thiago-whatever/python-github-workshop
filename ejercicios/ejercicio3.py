# Ejercicio 3: Juego de Adivinanza
# ---------------------------------
# Instrucciones:
# 1. Escribe un programa donde la computadora elija un número al azar entre 1 y 10.
#    - Para generar números aleatorios, utiliza la librería `random`.
# 2. El usuario debe intentar adivinar el número.
# 3. El programa debe:
#    - Indicar si el número ingresado es mayor o menor que el número correcto.
#    - Continuar pidiendo intentos hasta que el usuario adivine el número.
# 4. Al acertar, el programa debe imprimir un mensaje de felicitación y la cantidad de intentos realizados.
#    Ejemplo:
#    Adivina el número (entre 1 y 10): 7
#    El número es menor. Intenta de nuevo: 4
#    ¡Felicidades! Adivinaste el número 4 en 2 intentos.